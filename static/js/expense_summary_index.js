function getStatsDate() {
    const currentDate = new Date();
    const thisYear = currentDate.getFullYear()
    const lastYear = thisYear - 1
    fetch('/expense/year_category_stats/' + thisYear).then((res) => res.json()).then((data) => {
        thisYearStats(data.response_data)
    })
    fetch('/expense/year_category_stats/' + lastYear).then((res) => res.json()).then((data) => {
        lastYearStats(data.response_data)

    })
}


document.onload = getStatsDate()